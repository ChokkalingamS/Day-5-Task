// Find the Perimeter of a Rectangle
// Create a function that takes height and width and finds the perimeter of a rectangle.
// Examples
// findPerimeter(6, 7) ➞ 26
// findPerimeter(20, 10) ➞ 60
// findPerimeter(2, 9) ➞ 22



let m=(l,w)=>
{
  return 2*(l+w);
}
console.log(m(6,7));
console.log(m(20,10));
console.log(m(2,9));