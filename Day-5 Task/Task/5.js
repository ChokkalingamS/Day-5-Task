// Create a function that takes a number as an argument, increments the number by +1 and returns the result.
// Examples
// nextNumber(0) ➞ 1
// nextNumber(9) ➞ 10
// nextNumber(-3) ➞ -2





let sum=function(n)
{
  return n+=1;
}
console.log(sum(0));
console.log(sum(9));
console.log(sum(-3));
